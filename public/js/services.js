function capitalize() {
    return function(input) {
        if(input){
            var inputArray = input.split(" ")
            inputArray[0] = inputArray[0].charAt(0).toUpperCase() + inputArray[0].substr(1);
            input = inputArray.join(" ")
        }
        return input || "";
    }
}



